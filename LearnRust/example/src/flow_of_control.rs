pub fn look_if_else() {
  let n = 5;

  if n < 0 {
      print!("{} is negative", n);
  } else if n > 0 {
      print!("{} is positive", n);
  } else {
      print!("{} is zero", n);
  }

  // 가장 중요한 차이는 if/else가 expression이라는 점이다. 
  let big_n =
      if n < 10 && n > -10 {
          println!(", and is a small number, increase ten-fold");

          // This expression returns an `i32`.
          10 * n
      } else {
          println!(", and is a big number, halve the number");

          // This expression must return an `i32` as well.
          n / 2
          // TODO ^ Try suppressing this expression with a semicolon.
      };
  //   ^ Don't forget to put a semicolon here! All `let` bindings need it.

  println!("{} -> {}", n, big_n);
}

pub fn look_loop() {
    let mut count = 0_u32;

    println!("Let's count until infinity!");

    // Infinite loop
    loop {
        count += 1;

        if count == 3 {
            println!("three");

            // Skip the rest of this iteration
            continue;
        }

        println!("{}", count);

        if count == 5 {
            println!("OK, that's enough");

            // Exit this loop
            break;
        }
    }
}

pub fn look_loop_value() {
    let mut counter = 0;

    let result = loop {
        counter += 1;

        if counter == 10 {
            break counter * 2;
        }
    };

    assert_eq!(result, 20);
}

pub fn look_range_for() {
    // =100 means inclusive
    // `n` will take the values: 1, 2, ..., 100 in each iteration
    for n in 1..=10 {
        if n % 15 == 0 {
            println!("fizzbuzz");
        } else if n % 3 == 0 {
            println!("fizz");
        } else if n % 5 == 0 {
            println!("buzz");
        } else {
            println!("{}", n);
        }
    }
}

pub fn look_iter_for() {
    let names = vec!["Bob", "Frank", "Ferris"];

    for name in names.iter() {
        match name {
            &"Ferris" => println!("There is a rustacean among us!"),
            // TODO ^ Try deleting the & and matching just "Ferris"
            _ => println!("Hello {}", name),
        }
    }
    
    println!("names: {:?}", names);
}

pub fn look_iter_mut_for() {
    let mut names = vec!["Bob", "Frank", "Ferris"];

    // iter_mut()는 IterMut()를 리턴. IterMut는 end를 *mut로 갖고 있다.  
    for name in names.iter_mut() {
        *name = match name {
            &mut "Ferris" => "There is a rustacean among us!",
            _ => "Hello",
        }
    }

    println!("names: {:?}", names);
}

pub fn look_match_tuple_destructuring() {
    let triple = (3, -2, 4);
    // TODO ^ Try different values for `triple`

    println!("Tell me about {:?}", triple);
    // Match can be used to destructure a tuple
    match triple {
        // Destructure the second and third elements
        (0, y, z) => println!("First is `0`, `y` is {:?}, and `z` is {:?}", y, z),
        (1, ..)  => println!("First is `1` and the rest doesn't matter"),
        (.., 2)  => println!("last is `2` and the rest doesn't matter"),
        (3, .., 4)  => println!("First is `3`, last is `4`, and the rest doesn't matter"),
        // `..` can be used to ignore the rest of the tuple
        _      => println!("It doesn't matter what they are"),
        // `_` means don't bind the value to a variable
    }
}

pub fn look_match_array_destructuring() {
    // Try changing the values in the array, or make it a slice!
    let array = [3, -2, 6];

    match array {
        // Binds the second and the third elements to the respective variables
        [0, second, third] =>
            println!("array[0] = 0, array[1] = {}, array[2] = {}", second, third),

        // Single values can be ignored with _
        [1, _, third] => println!(
            "array[0] = 1, array[2] = {} and array[1] was ignored",
            third
        ),

        // You can also bind some and ignore the rest
        [-1, second, ..] => println!(
            "array[0] = -1, array[1] = {} and all the other ones were ignored",
            second
        ),
        // The code below would not compile
        // [-1, second] => ...

        // @ .. 이 문법적으로 특이하지만 rust는 이런 경향이 있다. 여기서는 배열을 의미한다.  [i32; 2]로 추론 
        // Or store them in another array/slice (the type depends on
        // that of the value that is being matched against)
        [3, tail @ ..] => println!(
            "array[0] = 3, and the other elements were {:?}",
            tail
        ),

        // Combining these patterns, we can, for example, bind the first and
        // last values, and store the rest of them in a single array
        [first, middle @ .., last] => println!(
            "array[0] = {}, middle = {:?}, array[2] = {}",
            first, middle, last
        ),
    }
}

